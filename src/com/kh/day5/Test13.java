public class Test13 {
    public static void main(String[] args) {
        System.out.println((int)5.6 + 3.5); // 5 + 3.5
        System.out.println((int)5.6 + (int)3.5);    // 5 + 3
        System.out.println((int)(5.6 + 3.5));   // 9.1
        System.out.println(7/4);    // 1
        System.out.println((double)3/2);    // 1.5
        System.out.println((double)(3/2));  // 1
    }
}


